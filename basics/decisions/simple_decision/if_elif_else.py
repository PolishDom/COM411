
paintdirection = input("What direction would you like to paint in? ")

if paintdirection == "Up" or paintdirection == "up":
  print ("PAINTING UP")

elif paintdirection == "Right" or paintdirection == "right":
  print ("PAINTING RIGHT")

elif paintdirection == "Left" or paintdirection == "left":
  print ("PAINTING LEFT")

elif paintdirection == "Down" or paintdirection == "down":
  print ("PAINTING DOWN")

else:
  print ("I DONT KNOW WHAT DIRECTION?!?!")

print ("Well done on painting")